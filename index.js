function increment(a) {
    return ++a;
  }
//console.log(increment(161));

function decrement(a){
  return --a;
}
//console.log(decrement(150));

function makeInt(a){
  return parseInt(a , 10);
}

function preserveDecimal(a){
  return parseFloat(a);
}
